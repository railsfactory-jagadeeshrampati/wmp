class Telugu_calendar
def self.days1 num
   if num == "Monday"
      return "SOMAVARAM"
   end   
   if num == "Tuesday"
      return "MANGALAVARAM"
   end   
   if num == "Wednesday"
       return "BUDHAVARAM"
   end   
   if num == "Thursday"
     return "GURUVARAM"
  end   
   if num == "Friday"
      return "SUKRUVARAM" 
  end  
   if num == "Saturday"
      return "SENIVARAM"
  end   
   if num == "Sunday"
     return  "AADHIVARAM"   
end
end
end
     
